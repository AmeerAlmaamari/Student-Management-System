    private void manage_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manage_deleteActionPerformed
        
        if(manage_id_field.getText().equals("")){
        
        JOptionPane.showMessageDialog(null,"No Student is Selected","No Selection", JOptionPane.ERROR_MESSAGE);
        }
        else{
        
        int id = Integer.valueOf(manage_id_field.getText());
        stu.operations(3, id, null, null, null, null);}
        
        stu.fill_stu_table(manage_table, "");
        manage_table.setModel(new DefaultTableModel(null, new Object[]{"ID","First Name", "Last Name", "Gender", "Phone"}));
        stu.fill_stu_table(manage_table,manage_search_field.getText());
        Stats.n_stu.setText(Integer.toString(MyFunction.data_count("student")));
        
        manage_id_field.setText("");
        manage_fn_field.setText("");
        manage_ln_field.setText("");
        manage_phone_field.setText("");
        manage_female_button.setSelected(false);
        manage_male_button.setSelected(false);
      
    }//GEN-LAST:event_manage_deleteActionPerformed

