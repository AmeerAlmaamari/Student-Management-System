    public boolean student_verify(){
    
        if(manage_fn_field.getText().equals("") ||
           manage_ln_field.getText().equals("") ||
           manage_phone_field.getText().equals("") ||
           manage_id_field.getText().equals("")){
            JOptionPane.showMessageDialog(null,"There are empty fields","Empty Fields",JOptionPane.ERROR_MESSAGE);
            
            
        return false;}
        
        else{return true;}
    
    }
    
    private void manage_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manage_editActionPerformed

        String f_name = manage_fn_field.getText();
        String l_name = manage_ln_field.getText();
        String phone = manage_phone_field.getText();
        int id = Integer.valueOf(manage_id_field.getText());
        String gender = "Male";
        if(manage_female_button.isSelected()){
            gender = "Female";}
//        } else if(manage_male_button.isSelected()){
//            gender = "Male";
//        }
        if(student_verify()){
            stu.operations(2, id, f_name, l_name, gender, phone);
            
            Manage_student.manage_table.setModel(new DefaultTableModel(null,new Object[]{"ID","First Name", "Last Name", "Gender", "Phone"}));
            stu.fill_stu_table(Manage_student.manage_table, "");

        }

    }//GEN-LAST:event_manage_editActionPerformed
    
