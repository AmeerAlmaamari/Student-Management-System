    private void manage_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_manage_tableMouseClicked

        int table_index = manage_table.getSelectedRow();
        manage_female_button.setSelected(false);
        manage_male_button.setSelected(true);
        
        DefaultTableModel model = (DefaultTableModel)manage_table.getModel();
        
        manage_id_field.setText(model.getValueAt(table_index, 0).toString());
        manage_fn_field.setText(model.getValueAt(table_index, 1).toString());
        manage_ln_field.setText(model.getValueAt(table_index, 2).toString());
        manage_phone_field.setText(model.getValueAt(table_index, 4).toString());

        if(model.getValueAt(table_index, 3).toString().equals("Female")){
        manage_female_button.setSelected(true);
        } else{manage_male_button.setSelected(true);}
    }//GEN-LAST:event_manage_tableMouseClicked

